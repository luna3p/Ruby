class DimentionArray
  #↓の場合だとある配列要素のの値を変化させた場合違う要素も変化してしまう。
  a=Array.new(3,[0,0,0])
  p a   #=>[[0, 0, 0], [0, 0, 0], [0, 0, 0]]
  a[0][1]=2
  p a   #=>[[0, 2, 0], [0, 2, 0], [0, 2, 0]]

  #↓の場合は問題なく処理する。
  b=Array.new(3){[0,0,0]}
  p b   #=>[[0, 0, 0], [0, 0, 0], [0, 0, 0]]
  b[0][1]=2
  p b   #=>[[0, 2, 0], [0, 0, 0], [0, 0, 0]]
end